    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

            Usuario u = getFormulario();//recupera os dados do formulÃ¡rio
            if(u != null){

                try {

                    u.setDtCadastro(Calendar.getInstance());
                    pnlUsuario.getControle().getConexaoJDBC().persist(u);
                    JOptionPane.showMessageDialog(this, "Usuario armazenado!", "Salvar", JOptionPane.INFORMATION_MESSAGE);

                    pnlUsuario.showTela("tela_listagem");//muda a tela para a listagem

                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Erro ao salvar Usuário! : "+ex.getMessage(), "Salvar", JOptionPane.ERROR_MESSAGE);
                    ex.printStackTrace();
                }

            }else{

                JOptionPane.showMessageDialog(this, "Preencha o formulário!", "Edição", JOptionPane.INFORMATION_MESSAGE);
            }


    }//GEN-LAST:event_btnSalvarActionPerformed

