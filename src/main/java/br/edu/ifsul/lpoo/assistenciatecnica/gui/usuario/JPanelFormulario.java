            public Usuario getFormulario(){
        
                        //validacao do formulario
                         if(txfCPF.getText().trim().length() == 11 && 
                            new String(psfSenha.getPassword()).trim().length() > 3 && 
                            txfNome.getText().trim().length() > 0){

                            Usuario u = new Usuario();
                            u.setCpf(txfCPF.getText().trim());
                            u.setNome(txfNome.getText().trim());
                            u.setSenha(new String(psfSenha.getPassword()).trim());
                            if(cbxCidade.getSelectedIndex() > 0){//verifica se o Usuário indicou alguma cidade para o registro.
                                u.setCidade((Cidade) cbxCidade.getSelectedItem());
                            } 
                            u.setTipoPessoa(tipoPessoa);

                            return u;
                         }

                         return null;
            }
            
            public void populaFormulario(Usuario u){
        
                        if(u == null){//se o parametro estiver nullo, limpa o formulario
                            txfCPF.setText("");
                            txfNome.setText("");
                            cbxCidade.setSelectedIndex(0);
                            psfSenha.setText(""); 
                            tipoPessoa = null;

                        }else{
                            txfCPF.setText(u.getCpf());
                            txfNome.setText(u.getNome());
                            cbxCidade.getModel().setSelectedItem(u.getCidade());// o combobox retorna um objeto (aqui chama o equals da classe Cidade)
                            psfSenha.setText(u.getSenha());
                            tipoPessoa = u.getTipoPessoa();
                        }
        
            }
            
            public void populaComboCidade(){
        
                        cbxCidade.removeAllItems();//zera o combo

                        DefaultComboBoxModel model =  (DefaultComboBoxModel) cbxCidade.getModel();

                        model.addElement("Selecione"); //primeiro item        
                        try {

                            List<Cidade> listCidades = controle.getConexaoJDBC().getCidades(null);
                            for(Cidade c : listCidades){
                                model.addElement(c);
                            }

                        } catch (Exception ex) {

                            JOptionPane.showMessageDialog(this, "Erro ao listar Cidades -:"+ex.getLocalizedMessage(), "Cidades", JOptionPane.ERROR_MESSAGE);
                            ex.printStackTrace();
                        }                
            }
    

